    private void DoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DoneActionPerformed
        if (Username.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter a username.");
            return;
        }
        if (Password.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter a password.");
            return;
        }
        if (Cpassword.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please confirm the password.");
            return;
        }
        if (Name.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter your name.");
            return;
        }
        if (DOB.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter your date of birth.");
            return;
        }
        if (Email.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter your email.");
            return;
        }
        if (Number.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please enter your phone number.");
            return;
        }
        if (!(Password.getText().equals(Cpassword.getText()))) {
            JOptionPane.showMessageDialog(this, "The 2 passwords do not match.");
            Password.setText("");
            Cpassword.setText("");
            return;
        }
        if (Password.getText().length() < 6) {
            JOptionPane.showMessageDialog(this, "The password should contain at least 6 characters.");
            Password.setText("");
            Cpassword.setText("");
            return;
        }
        EmailValidator E = new EmailValidator();
        if (!E.validate(Email.getText())) {
            JOptionPane.showMessageDialog(this, "Invalid Email ID.");
            return;
        }
        Check();
    }//GEN-LAST:event_DoneActionPerformed

