    public void Check()
    {
        String sql="SELECT * FROM USERS";
        Connection conn = null;
        Statement stmt = null;
        try {
            //Register the JDBC driver
            Class.forName("com.mysql.jdbc.Driver");

            // Open a connection
            conn = DriverManager.getConnection(DB_URL, USER, PASS);
            System.out.println("Connected to database.");

            //Execute query
            stmt = conn.createStatement();

            ResultSet rs=stmt.executeQuery(sql);
            while(rs.next())
            {
                if(rs.getString("USERNAME").toLowerCase().equals(Username.getText().toLowerCase()))
                {
                    JOptionPane.showMessageDialog(this, "Username already is use.");
                    return;
                }
            }
            Save();
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
        finally {
            try {
                if (conn != null) {
                    conn.close();
                }
            } catch (SQLException se) {
                se.printStackTrace();
            }
        }
    }
    
